let theBoard, buttonArray;
let edgeHorizontalBuffer, edgeVerticalBuffer;
let cellHorizontalBuffer, cellVerticalBuffer;
let cellWidth, cellHeight;
let state;

function setup() {
  createCanvas(windowWidth, windowHeight);
  theBoard = random(boards);
  setBufferValues();
  buttonArray = createButtons(theBoard);
  state = "board set";
}

function draw() {
  if (state === "board set") {
    background("#2a9d8f");
  }
  else if (state === "waiting for choice") {
    background("#e9c46a");
  }
  
  for (let button of buttonArray) {
    button.display();
  }
}

function windowResized() {
  resizeCanvas(windowWidth, windowHeight);
  setBufferValues();
  buttonArray = createButtons(theBoard);
}

function mousePressed() {
  if (state === "board set") {
    let clickedAButton = false;
    for (let button of buttonArray) {
      if (button.handleClick(mouseX, mouseY)) {
        clickedAButton = true;
      }
    }

    if (clickedAButton) {
      state = "waiting for choice";
    }
  }
}

function setBufferValues() {
  edgeHorizontalBuffer = width * 0.1;
  edgeVerticalBuffer = height * 0.1;

  cellHorizontalBuffer = width * 0.05;
  cellVerticalBuffer = height * 0.05;

  cellWidth = (width - edgeHorizontalBuffer*2 - cellHorizontalBuffer*5)/6;
  cellHeight = (height - edgeVerticalBuffer*2 - cellVerticalBuffer*5)/6;
}

function createButtons(startingValues) {
  let theButtons = [];
  let startingState;
  for (let y=0; y<6; y++) {
    for (let x=0; x<6; x++) {
      let oneDimensionalArrayLocation = y*6 + x;
      if (startingValues[oneDimensionalArrayLocation] === 0) {
        startingState = true;
      }
      else {
        startingState = false;
      }
      let someButton = new Button((cellWidth+cellHorizontalBuffer)*x+edgeHorizontalBuffer, (cellHeight+cellVerticalBuffer)*y+edgeVerticalBuffer, cellWidth, cellHeight, startingState);
      theButtons.push(someButton);
    }
  }
  return theButtons;
}

class Button {
  constructor(x, y, w, h, state) {
    this.x = x;
    this.y = y;
    this.width = w;
    this.height = h;
    this.state = state;
  }

  display() {
    if (this.state === true) {
      fill("white");
    }
    else {
      fill("black");
    }
    rect(this.x, this.y, this.width, this.height);
  }

  handleClick(x, y) {
    if (this.checkIfInside(x, y)) {
      this.state = !this.state;
      return true;
    }
  }

  checkIfInside(x, y) {
    return x >= this.x && x <= this.x + this.width &&
           y >= this.y && y <= this.y + this.height;
  }
}

